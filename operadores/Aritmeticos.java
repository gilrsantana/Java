package fundamentos.operadores;

public class Aritmeticos {
	public static void main(String[] args) {
		double x = 8.3;
		double y = 5.2;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		
		int a = 8;
		int b = 5;
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
		System.out.println(a / (double)b);
		System.out.println(a / (float)b);
		
		

	}
}
